public class ejemploFor {


    public static void main(String[] args) {
        int res=0;
        int i;
        //Iniciar el ciclo de 1 a 10
        for (i=0; i<=10; i++) {
            res=i*2;
            System.out.println("2x"+ i +"="+res);
        }
    }
    
}
